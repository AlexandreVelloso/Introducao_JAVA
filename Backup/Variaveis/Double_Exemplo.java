public class Double_Exemplo{
  public static void main( String [] args ){
      double a,b;
      double r1,r2,r3,r4; // essas sao as variaveis que vai guardar os resultados das operacoes

      a = 10.5;
      b = 3.14;

      // adicao
      r1 = a + b;
      // subtracao
      r2 = a - b;
      // multiplicacao
      r3 = a * b;
      // divisao
      r4 = a / b;

      System.out.println( "Valor de r1: "+r1);
      System.out.println( "Valor de r2: "+r2);
      System.out.println( "Valor de r3: "+r3);
      System.out.println( "Valor de r4: "+r4);

      /*
      Esse e' um comentario em bloco, entao o programa nao vai executar esse trecho

      r1: 13.64
      r2: 7.359999999999999
      r3: 32.97
      r4: 3.343949044585987
      */
  }
}
