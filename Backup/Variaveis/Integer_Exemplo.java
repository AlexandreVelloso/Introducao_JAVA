public class Integer_Exemplo{
  public static void main(String[] args){

    int a,b;
    int r1,r2,r3,r4,r5; // essas sao as variaveis que vao guardar os resultados das operacoes

    a = 10; // aqui eu estou colocando o valor 10 na variavel a
    b = 3;

    // adicao
    r1 = a + b;
    // subtracao
    r2 = a - b;
    // multiplicacao
    r3 = a * b;
    // divisao
    r4 = a / b;
    // modulo
    r5 = a % b;

    System.out.println( "Valor de r1: "+r1);
    System.out.println( "Valor de r2: "+r2);
    System.out.println( "Valor de r3: "+r3);
    System.out.println( "Valor de r4: "+r4);
    System.out.println( "Valor de r5: "+r5);

    /*
    Esse e' um comentario em bloco, entao o programa nao vai executar esse trecho

    r1: 13
    r2: 7
    r3: 30
    r4: 3
    r5: 1
    */
  }
}
