public class Char_Exemplo{
  public static void main( String [] args ){

      // Declarar variaveis
      char c1,c2,c3;

      // Dar valor as variaveis, note que o valor de um char deve estar entre aspas simples: ''
      c1 = 'a';
      c2 = '0';
      c3 = '$';

      System.out.println( "Valor de c1: "+c1);
      System.out.println( "Valor de c2: "+c2);
      System.out.println( "Valor de c3: "+c3);

      /*
      Esse e' um comentario em bloco, entao o programa nao vai executar esse trecho

      c1: a
      c2: 0
      c3: $
      */
  }
}
