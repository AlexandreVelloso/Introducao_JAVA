public class Variaveis{
  public static void main( String [] args ){

      /*
      OBS: Os nomes nao precisam ser esses, eu coloquei o tipo da variaveil
      antes para facilitar a leitura do programa.
      */

      // *** INTEIRO ***
      int int_a, int_b, int_c;
      int_a = 10;
      int_b = 20;
      int_c = int_a + int_b;

      System.out.println("Inteiro A: "+int_a);
      System.out.println("Inteiro B: "+int_b);
      System.out.println("Inteiro C: "+int_c);

      // *** DOUBLE ***
      double double_x, double_y, double_z;
      double_a = 5.0;
      double_b = 3.14;
      double_c = double_a + double_b;

      System.out.println("Real A: "+double_a);
      System.out.println("Real B: "+double_b);
      System.out.println("Real C: "+double_c);

      // *** CHAR ***
      char char_a;
      char_a = 'v';

      System.out.println("Char A: "+char_a);

      // *** STRING ***
      String str_a, str_b, str_c;
      str_a = "Ola";
      str_b = "Mundo!";
      str_c = str_a + str_b;

      System.out.println("String A: "+str_a);
      System.out.println("String B: "+str_b);
      System.out.println("String C: "+str_c);

      // *** BOOLEAN ***
      boolean bool_a, bool_b;
      bool_a = true;
      bool_b = false;

      System.out.println("Boolean A: "+bool_a);
      System.out.println("Boolean B: "+bool_b);


  }
}
